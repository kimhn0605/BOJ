# 방법 1
n = int(input())
box = 0

while True:
    if (n % 5) == 0:             # n 이 5로 나누어 떨어진다면 (나머지가 0 이라면)
      box = (box + n // 5)       # 기존 box 개수에 5로 나눈 몫을 누적으로 더하여 출력하고 while 문 탈출
      print(box)           
      break
      
    # 5의 배수가 될 때까지 n-3, box+1
    n -= 3                    
    box += 1
    
    if n < 0:         # n 이 0 보다 작아졌다면 -1 출력
        print("-1")
        break
      
